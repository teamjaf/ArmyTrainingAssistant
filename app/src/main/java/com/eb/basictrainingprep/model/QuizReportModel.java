package com.eb.basictrainingprep.model;

public class QuizReportModel {

    String quizDate;
    String quizStatus;
    String quizType;
    String quizScore;
    String quizCorrects;
    String quizIncorrects;

    public QuizReportModel() {
    }

    public String getQuizDate() {
        return quizDate;
    }

    public void setQuizDate(String quizDate) {
        this.quizDate = quizDate;
    }

    public String getQuizStatus() {
        return quizStatus;
    }

    public void setQuizStatus(String quizStatus) {
        this.quizStatus = quizStatus;
    }

    public String getQuizType() {
        return quizType;
    }

    public void setQuizType(String quizType) {
        this.quizType = quizType;
    }

    public String getQuizScore() {
        return quizScore;
    }

    public void setQuizScore(String quizScore) {
        this.quizScore = quizScore;
    }

    public String getQuizCorrects() {
        return quizCorrects;
    }

    public void setQuizCorrects(String quizCorrects) {
        this.quizCorrects = quizCorrects;
    }

    public String getQuizIncorrects() {
        return quizIncorrects;
    }

    public void setQuizIncorrects(String quizIncorrects) {
        this.quizIncorrects = quizIncorrects;
    }
}
