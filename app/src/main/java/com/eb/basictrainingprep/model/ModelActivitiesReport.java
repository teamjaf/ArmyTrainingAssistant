package com.eb.basictrainingprep.model;

public class ModelActivitiesReport {
    String date;
    String pushups;
    String situps;
    String mileRun;
    String note;

    public ModelActivitiesReport() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPushups() {
        return pushups;
    }

    public void setPushups(String pushups) {
        this.pushups = pushups;
    }

    public String getSitups() {
        return situps;
    }

    public void setSitups(String situps) {
        this.situps = situps;
    }

    public String getMileRun() {
        return mileRun;
    }

    public void setMileRun(String mileRun) {
        this.mileRun = mileRun;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
