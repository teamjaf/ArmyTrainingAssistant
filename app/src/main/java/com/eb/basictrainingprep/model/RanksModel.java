package com.eb.basictrainingprep.model;

public class RanksModel {
    String rankCatName;
    String rankTitle;
    String rankAbr;
    String rankPayGrade;
    String rankRemarks;

    public RanksModel() {
    }

    public String getRankCatName() {
        return rankCatName;
    }

    public void setRankCatName(String rankCatName) {
        this.rankCatName = rankCatName;
    }

    public String getRankTitle() {
        return rankTitle;
    }

    public void setRankTitle(String rankTitle) {
        this.rankTitle = rankTitle;
    }

    public String getRankAbr() {
        return rankAbr;
    }

    public void setRankAbr(String rankAbr) {
        this.rankAbr = rankAbr;
    }

    public String getRankPayGrade() {
        return rankPayGrade;
    }

    public void setRankPayGrade(String rankPayGrade) {
        this.rankPayGrade = rankPayGrade;
    }

    public String getRankRemarks() {
        return rankRemarks;
    }

    public void setRankRemarks(String rankRemarks) {
        this.rankRemarks = rankRemarks;
    }
}
