package com.eb.basictrainingprep.model;

public class ModelRecruitList {
    String recruitName;
    String recruitEnrollDate;
    String recruitUid;


    public ModelRecruitList() {
    }

    public String getRecruitName() {
        return recruitName;
    }

    public void setRecruitName(String recruitName) {
        this.recruitName = recruitName;
    }

    public String getRecruitEnrollDate() {
        return recruitEnrollDate;
    }

    public void setRecruitEnrollDate(String recruitEnrollDate) {
        this.recruitEnrollDate = recruitEnrollDate;
    }

    public String getRecruitUid() {
        return recruitUid;
    }

    public void setRecruitUid(String recruitUid) {
        this.recruitUid = recruitUid;
    }
}
